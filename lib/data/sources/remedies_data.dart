/// Get all remedy categories
List<String> getRemedyCategories() {
  return remediesData.map((r) => r.category).toSet().toList()..sort();
}

/// Get remedies by category
List<Remedy> getRemediesByCategory(String category) {
  if (category == 'All') return remediesData;
  return remediesData.where((r) => r.category == category).toList();
}

/// Search remedies
List<Remedy> searchRemedies(String query) {
  final q = query.toLowerCase();
  return remediesData.where((r) =>
    r.title.toLowerCase().contains(q) ||
    r.description.toLowerCase().contains(q) ||
    r.healthGoals.any((g) => g.toLowerCase().contains(q))
  ).toList();
}
